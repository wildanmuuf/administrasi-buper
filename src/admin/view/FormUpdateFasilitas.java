    private String tampilkanTanggalDanWaktu(Date tanggalDanWaktu,
            String pola) {
        String tanggalStr = null;
        SimpleDateFormat formatter = null;
        formatter = new SimpleDateFormat(pola);
 
        tanggalStr = formatter.format(tanggalDanWaktu);
        return tanggalStr;
    }
    
